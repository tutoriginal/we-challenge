#include "libft.h"

int			ft_isprintf(int n)
{
	if (n == 'c' || n == 's' || n == 'p' || n == 'd'
	|| n == 'i' || n == 'u' || n == 'x' || n == 'X' || n == '%')
		return (1);
	return (0);
}
